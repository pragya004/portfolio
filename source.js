var cards = document.querySelectorAll('.card');
var heading = document.querySelectorAll('.heading');
var content = document.querySelectorAll('.content');
var view = document.querySelectorAll('.view');
var git = document.querySelectorAll('.git');
var proskills = document.querySelectorAll('.proskills');


cards[0].addEventListener("mouseover", function( e )
{
	heading[0].style.visibility='hidden';
	content[0].style.visibility='hidden';
	view[0].style.display='block';
	git[0].style.display='block';
	proskills[0].style.visibility='hidden';
});

cards[0].addEventListener("mouseout", function( e )
{
	heading[0].style.visibility='visible';
	content[0].style.visibility='visible';
	view[0].style.display='none';
	git[0].style.display='none';
	proskills[0].style.visibility='visible';
});
